    private void btnInserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserActionPerformed
        producto.getProducto().setCodigoProducto(Integer.parseInt(txtId.getText()));
        producto.getProducto().setNombreProducto(txtNombre.getText());
        producto.getProducto().setDescripcionProducto(txtDesc.getText());
        producto.getProducto().setCodigoMarca(Integer.parseInt(txtCod_Mar.getText()));
        producto.getProducto().setCodigoCategoria(Integer.parseInt(txtCod_Cat.getText()));
        producto.getProducto().setStockProducto(Integer.parseInt(txtStock.getText()));
        
        
        try {
         producto.Insertar();
         JOptionPane.showMessageDialog(this, "Dato insertado correctamente");
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
    }//GEN-LAST:event_btnInserActionPerformed

